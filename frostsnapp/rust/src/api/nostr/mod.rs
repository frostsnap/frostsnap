/// Per-consumer handle for a connected channel. Owns its own
/// `ChannelRunner` + relay subscription + broadcast stream. The
/// caller (chat_page, post-keygen verify, etc.) holds this for the
/// lifetime of their session and calls `close()` to tear down.
///
/// Lifecycle is **two-phase** to avoid the lost-startup-events race
/// (codex feedback on commit 0ac5d35): `connect_to_channel` returns a
/// dormant handle; the caller attaches its `events().watch()`
/// listener, *then* awaits `start()`. The runner is spawned inside
/// `start()` — after the broadcast has subscribers — so the
/// `Connecting`/`Connected` emits and cache replay are observed.
///
/// FRB-opaque: send methods are callable on it directly, and
/// `events()` returns the non-opaque `ChannelEventBcast` leaf the
/// caller `watch()`es to get the event stream.
#[frb(opaque)]
pub struct ChannelHandle {
    bcast: Broadcast<ChannelEvent>,
    key_context: KeyContext,
    nostr_client: Client,
    identity: NostrIdentity,
    /// `Some(client)` until the first successful `start()` takes it.
    pending: tokio::sync::Mutex<Option<ChannelClient>>,
    /// Set once `start()` finishes its inner `ChannelClient::run`.
    inner: tokio::sync::OnceCell<InnerChannelHandle>,
}

impl ChannelHandle {
    /// Return the broadcast leaf. Dart calls `events().watch()` to
    /// get the live `Stream<ChannelEvent>`. Each call mints a fresh
    /// leaf wrapping an Arc-clone of the same underlying broadcast,
    /// so multiple `watch()` calls all see the same events.
    #[frb(sync)]
    pub fn events(&self) -> ChannelEventBcast {
        ChannelEventBcast::new(self.bcast.clone())
    }

    /// Spawn the per-channel runner and begin emitting into the
    /// broadcast. **Must be awaited after** at least one listener has
    /// been attached via `events().watch().listen(...)`, otherwise
    /// `Connecting`/`Connected` and cache-replay events emitted during
    /// startup will be dropped on the floor.
    ///
    /// Idempotent: second and later calls return `Ok(())` immediately.
    pub async fn start(&self) -> Result<()> {
        if self.inner.initialized() {
            return Ok(());
        }
        let mut pending = self.pending.lock().await;
        if self.inner.initialized() {
            return Ok(());
        }
        let cc = pending
            .take()
            .ok_or_else(|| anyhow!("channel handle has no pending client"))?;
        let sink = BroadcastSink {
            bcast: self.bcast.clone(),
        };
        let inner = cc
            .run(self.nostr_client.clone(), sink, self.identity.clone())
            .await
            .map_err(|e| {
                tracing::error!(error = %e, "failed to start channel runner");
                e
            })?;
        // Publish-after-fold is handled inside ChannelRunner::run via
        // the identity's publish policy; no explicit call here.
        let _ = self.inner.set(inner);
        Ok(())
    }

    fn require_inner(&self) -> Result<&InnerChannelHandle> {
        self.inner
            .get()
            .ok_or_else(|| anyhow!("channel handle not started — call start() first"))
    }

    /// Send a chat message. `reply_to` is optional. Signs with the
    /// identity captured at `connect_to_channel` time — see
    /// [`NostrClient::connect_to_channel`].
    pub async fn send_message(
        &self,
        content: String,
        reply_to: Option<EventId>,
    ) -> Result<EventId> {
        let inner = self.require_inner()?;
        let keys = inner.runner_handle.signing_keys().clone();
        inner.send_message(content, reply_to, &keys).await
    }

    /// Share a receive address in the channel.
    pub async fn send_receive_address(
        &self,
        derivation_index: u32,
        memo: String,
    ) -> Result<EventId> {
        let inner = self.require_inner()?;
        let keys = inner.runner_handle.signing_keys().clone();
        inner
            .send_receive_address(&keys, derivation_index, memo)
            .await
    }

    /// Propose a transaction for signing.
    pub async fn send_sign_request(
        &self,
        unsigned_tx: super::signing::UnsignedTx,
        message: String,
    ) -> Result<EventId> {
        let inner = self.require_inner()?;
        let keys = inner.runner_handle.signing_keys().clone();
        let sign_task = WireSignTask::BitcoinTransaction(unsigned_tx.template_tx.clone());
        inner.send_sign_request(&keys, sign_task, message).await
    }

    /// Propose a test message for signing.
    pub async fn send_test_sign_request(
        &self,
        test_message: String,
        message: String,
    ) -> Result<EventId> {
        let inner = self.require_inner()?;
        let keys = inner.runner_handle.signing_keys().clone();
        let sign_task = WireSignTask::Test {
            message: test_message,
        };
        inner.send_sign_request(&keys, sign_task, message).await
    }

    pub async fn send_sign_offer(
        &self,
        request_id: EventId,
        binonces: Vec<ParticipantBinonces>,
    ) -> Result<EventId> {
        let inner = self.require_inner()?;
        let keys = inner.runner_handle.signing_keys().clone();
        inner.send_sign_offer(&keys, request_id, binonces).await
    }

    pub async fn send_sign_partial(
        &self,
        request_id: EventId,
        offer_subset: Vec<EventId>,
        shares: frostsnap_core::coordinator::ParticipantSignatureShares,
    ) -> Result<EventId> {
        let inner = self.require_inner()?;
        let keys = inner.runner_handle.signing_keys().clone();
        inner
            .send_sign_partial(&keys, request_id, offer_subset, shares)
            .await
    }

    pub async fn send_sign_cancel(&self, request_id: EventId) -> Result<EventId> {
        let inner = self.require_inner()?;
        let keys = inner.runner_handle.signing_keys().clone();
        inner.send_sign_cancel(&keys, request_id).await
    }

    /// Pump a wallet-stream tx into the channel runner so it can fold
    /// chat→tx correlations.
    pub async fn notify_tx_observed(&self, tx: crate::api::bitcoin::Transaction) -> Result<()> {
        let wallet_tx = frostsnap_coordinator::bitcoin::wallet::Transaction {
            inner: std::sync::Arc::new(tx.inner.clone()),
            txid: tx.inner.compute_txid(),
            confirmation_time: tx.confirmation_time.clone(),
            first_seen: tx.first_seen,
            last_seen: tx.last_seen,
            prevouts: tx.prevouts.clone(),
            is_mine: tx.is_mine.clone(),
        };
        self.require_inner()?.notify_tx_observed(wallet_tx).await
    }

    /// Build a `SealedSigningData` bundle when Dart receives a
    /// `SigningEvent::RoundConfirmed`. Uses the channel's
    /// `KeyContext` captured at connect time.
    #[frb(sync)]
    pub fn seal_round_confirmed(
        &self,
        request_id: EventId,
        sign_task: WireSignTask,
        binonces: Vec<ParticipantBinonces>,
    ) -> Result<SealedSigningData> {
        Ok(SealedSigningData {
            request_id: request_id.into(),
            sign_task,
            binonces,
            key_context: self.key_context.clone(),
        })
    }

    /// Signal the runner to shut down. Returns immediately
    /// (fire-and-forget). No-op if `start()` was never called
    /// (nothing to tear down yet). Call from Flutter `State.dispose()`
    /// since dispose can't be `async`. Forget to call it and the
    /// runner leaks to process exit — the caller owns the handle.
    #[frb(sync)]
    pub fn close(&self) {
        if let Some(inner) = self.inner.get() {
            inner.runner_handle.shutdown();
        }
    }
}


/// Unified Nostr client for profiles and channels.
/// Create once and keep around - cloning shares the connection pool.
pub struct NostrClient {
    client: Client,
}

impl NostrClient {
    /// Connect to default relays and return a client ready for use.
    pub async fn connect() -> Result<Self> {
        let database = get_nostr_lmdb()?;
        let client = Client::builder().database(database).build();

        for url in default_relay_urls() {
            if let Err(e) = client.add_relay(&url).await {
                tracing::warn!(relay = %url, error = %e, "failed to add relay");
            }
        }

        client.connect().await;

        Ok(Self { client })
    }

    /// Fetch profile metadata for a public key.
    /// Checks the local cache first, then fetches from relays if not found.
    /// Returns None if the user has no profile.
    pub async fn fetch_profile(&self, pubkey: &PublicKey) -> Result<Option<NostrProfile>> {
        self.fetch_profile_with_timeout(pubkey, Duration::from_secs(5))
            .await
    }

    async fn fetch_profile_with_timeout(
        &self,
        pubkey: &PublicKey,
        timeout: Duration,
    ) -> Result<Option<NostrProfile>> {
        let nostr_pk = (*pubkey).into();
        if let Ok(Some(metadata)) = self.client.database().metadata(nostr_pk).await {
            return Ok(Some(NostrProfile::from_metadata(*pubkey, metadata)));
        }
        match self.client.fetch_metadata(nostr_pk, timeout).await {
            Ok(Some(metadata)) => Ok(Some(NostrProfile::from_metadata(*pubkey, metadata))),
            Ok(None) => Ok(None),
            Err(e) => {
                tracing::debug!(pubkey = %pubkey, error = %e, "failed to fetch profile");
                Ok(None)
            }
        }
    }

    /// Mode A — validate that an imported nsec has a discoverable
    /// public NIP-01 kind 0, and return the fetched profile. Used by
    /// the Dart layer's Import button before calling
    /// `NostrSettings::set_imported_identity`. 30-second timeout
    /// because this is a one-time user-blocking operation.
    pub async fn fetch_profile_for_import(&self, nsec: String) -> Result<NostrProfile> {
        let keys = Keys::parse(&nsec)?;
        let pubkey: PublicKey = keys.public_key().into();
        match self
            .fetch_profile_with_timeout(&pubkey, IMPORT_PROFILE_FETCH_TIMEOUT)
            .await?
        {
            Some(profile) => Ok(profile),
            None => Err(anyhow!(
                "No public profile found for this nsec. \
                 Set one up in your usual nostr client \
                 (publish a kind 0 metadata event), then try importing again."
            )),
        }
    }

    /// Build a fresh per-consumer `ChannelHandle`. The handle is
    /// returned dormant — the runner is only spawned when the caller
    /// awaits `handle.start()`. This split lets Dart attach its
    /// `events().watch().listen(...)` listener *before* the runner
    /// emits `Connecting`, `Connected`, and the cache replay, so no
    /// startup events are dropped.
    ///
    /// No `StreamSink` param — FRB can't have a function that BOTH
    /// returns a value and exposes a Dart `Stream`. The Dart side
    /// gets the event stream via `handle.events().watch()`.
    pub async fn connect_to_channel(
        &self,
        identity: NostrIdentity,
        params: &ChannelConnectionParams,
    ) -> Result<ChannelHandle> {
        let key_context = params.key_context.clone();
        let channel_client =
            ChannelClient::new(params.key_context.clone(), params.init_data.clone());

        Ok(ChannelHandle {
            bcast: Broadcast::<ChannelEvent>::default(),
            key_context,
            nostr_client: self.client.clone(),
            identity,
            pending: tokio::sync::Mutex::new(Some(channel_client)),
            inner: tokio::sync::OnceCell::new(),
        })
    }

    /// Join a wallet from a nostr invite link. Fetches channel data from relays,
    /// adds the key to the coordinator, and returns the new wallet's KeyId.
    pub async fn join_from_link(
        &self,
        coord: &super::coordinator::Coordinator,
        channel_secret: ChannelSecret,
        encryption_key: SymmetricKey,
    ) -> Result<frostsnap_core::KeyId> {
        let channel_keys = ChannelKeys::from_channel_secret(&channel_secret);
        let runner = ChannelRunner::new(channel_keys.clone());
        let init_event = runner
            .fetch_init_event(&self.client)
            .await?
            .ok_or_else(|| anyhow!("no channel found for this link"))?;
        let init_data =
            frostsnap_nostr::channel_runner::decode_bincode::<ChannelInitData>(&init_event)?;

        let derived_channel_keys =
            ChannelKeys::from_access_structure_id(&init_data.access_structure_id());
        anyhow::ensure!(
            derived_channel_keys == channel_keys,
            "channel secret mismatch in channel init data"
        );

        let as_ref = {
            let mut db = coord.0.db.lock().unwrap();
            let mut coordinator = coord.0.coordinator.lock().unwrap();
            coordinator.staged_mutate(&mut *db, |coordinator| {
                Ok(coordinator.add_key_and_access_structure(
                    init_data.key_name.clone(),
                    init_data.root_shared_key.clone(),
                    init_data.purpose,
                    encryption_key,
                    &mut rand::thread_rng(),
                ))
            })?
        };

        coord.0.emit_key_state();
        Ok(as_ref.key_id)
    }

    /// Open a remote keygen lobby at `channel_secret` as the initiator.
    /// Publishes the nostr `ChannelCreation` event — with the wallet
    /// name + purpose carried inline in its content — and returns a
    /// handle the caller uses to register devices or cancel.
    pub async fn create_remote_lobby(
        &self,
        identity: NostrIdentity,
        channel_secret: ChannelSecret,
        key_name: String,
        purpose: KeyPurpose,
    ) -> Result<self::remote_keygen::RemoteLobbyHandle> {
        let keys = identity.keys()?;
        let lobby_client = LobbyClient::new(channel_secret.clone());
        let invite_link = channel_secret.keygen_invite_link();
        let metadata = LobbyChannelMetadata { key_name, purpose };
        let init_event = lobby_client.build_creation_event(&keys, &metadata).await?;
        let (bridge, sink) = self::remote_keygen::RemoteLobbyHandle::build_bridge();
        let handle = lobby_client
            .run(self.client.clone(), identity, Some(init_event), sink)
            .await?;
        Ok(self::remote_keygen::RemoteLobbyHandle::new(
            handle,
            keys,
            invite_link,
            self.client.clone(),
            bridge,
        ))
    }

    /// Join an existing remote keygen lobby. `channel_secret` comes from
    /// parsing the initiator's `frostsnap://channel/…` invite link
    /// (see `ChannelSecret::from_invite_link`).
    pub async fn join_remote_lobby(
        &self,
        identity: NostrIdentity,
        channel_secret: ChannelSecret,
    ) -> Result<self::remote_keygen::RemoteLobbyHandle> {
        let keys = identity.keys()?;
        let lobby_client = LobbyClient::new(channel_secret.clone());
        let invite_link = channel_secret.keygen_invite_link();
        let (bridge, sink) = self::remote_keygen::RemoteLobbyHandle::build_bridge();
        let handle = lobby_client
            .run(self.client.clone(), identity, None, sink)
            .await?;
        Ok(self::remote_keygen::RemoteLobbyHandle::new(
            handle,
            keys,
            invite_link,
            self.client.clone(),
            bridge,
        ))
    }

    /// Leader entry: creates a remote recovery lobby, publishes the
    /// NIP-28 `ChannelCreation` with encoded metadata, and returns a
    /// handle whose `invite_link()` is what the leader shares with
    /// joiners.
    pub async fn create_remote_recovery_lobby(
        &self,
        identity: NostrIdentity,
        channel_secret: ChannelSecret,
        key_name: String,
        purpose: KeyPurpose,
        threshold_hint: Option<u16>,
    ) -> Result<self::remote_recovery::RemoteRecoveryLobbyHandle> {
        use self::remote_recovery::{RecoveryBridge, RemoteRecoveryLobbyHandle};
        use frostsnap_nostr::recovery::{RecoveryChannelMetadata, RecoveryLobbyClient};

        let metadata = RecoveryChannelMetadata {
            key_name,
            purpose,
            threshold_hint,
        };
        let client = RecoveryLobbyClient::new(channel_secret.clone()).with_metadata(metadata);
        let invite_link = client.invite_link();
        let init_event = client.build_creation_event(&identity).await?;
        let bridge = RecoveryBridge::new();
        let sink = bridge.sink();
        let inner = client
            .run(self.client.clone(), identity, Some(init_event), sink)
            .await?;

        // Leader: broadcast can't seed synchronously because the ChannelCreation
        // hasn't been folded yet at this instant. Await the sink's first
        // StateChanged (matches the joiner arm-then-check pattern for symmetry
        // and gives the same guarantee: the handed-out handle carries a
        // populated broadcast).
        let broadcast = await_first_state(&bridge).await?;
        Ok(RemoteRecoveryLobbyHandle::from_bridge(
            inner,
            invite_link,
            self.client.clone(),
            broadcast,
            &bridge,
        ))
    }

    /// Joiner entry: awaits the leader's `ChannelCreation` (30s timeout)
    /// so the returned handle carries a broadcast seeded with real
    /// metadata.
    pub async fn join_remote_recovery_lobby(
        &self,
        identity: NostrIdentity,
        channel_secret: ChannelSecret,
    ) -> Result<self::remote_recovery::RemoteRecoveryLobbyHandle> {
        use self::remote_recovery::{RecoveryBridge, RemoteRecoveryLobbyHandle};
        use frostsnap_nostr::recovery::RecoveryLobbyClient;

        let client = RecoveryLobbyClient::new(channel_secret.clone());
        let invite_link = client.invite_link();
        let bridge = RecoveryBridge::new();
        let sink = bridge.sink();
        let inner = client
            .run(self.client.clone(), identity, None, sink)
            .await?;

        let broadcast = await_first_state(&bridge).await?;
        Ok(RemoteRecoveryLobbyHandle::from_bridge(
            inner,
            invite_link,
            self.client.clone(),
            broadcast,
            &bridge,
        ))
    }
}

/// Arm-then-check wait pattern: poll the shared broadcast slot,
/// arming the Notify's `notified()` before each read so a
/// concurrently-firing `notify_waiters` isn't missed. See plan
/// §"Emission gate" for the rationale.
async fn await_first_state(
    bridge: &self::remote_recovery::RecoveryBridge,
) -> Result<BehaviorBroadcast<frostsnap_nostr::recovery::RecoveryLobbySnapshot>> {
    tokio::time::timeout(std::time::Duration::from_secs(30), async {
        loop {
            let notified = bridge.ready.notified();
            tokio::pin!(notified);
            notified.as_mut().enable();

            if *bridge.cancelled_pre_state.lock().unwrap() {
                return Err(anyhow!(
                    "recovery channel malformed — metadata decode failed"
                ));
            }
            if let Some(bcast) = bridge.broadcast.lock().unwrap().as_ref().cloned() {
                return Ok(bcast);
            }
            notified.await;
        }
    })
    .await
    .map_err(|_| anyhow!("ChannelCreation didn't arrive within 30s"))?
}

